package com.myjava.ocp.lab02;

public class BitDemo {
    public static void main(String[] args) {
        int x = 13;
        System.out.println(Integer.toBinaryString(x));
        int y = 0b1101;
        System.out.println(y);
        int z = -14;
        System.out.println(Integer.toBinaryString(z));
    }
}
